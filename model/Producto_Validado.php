class Producto_Validado extends MyPDO
{
    public $id, $nombre;
    
    public $table = "producto_validado";
    
    public function __construct() {}
    
    function getId() {
        return $this->id;
    }

    function getNombre() {
        return $this->nombre;
    }

    function setId($id) {
        $this->id = $id;
    }

    function setNombre($nombre) {
        $this->nombre = $nombre;
    }

    
}